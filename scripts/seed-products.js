async function createMetafieldsForProduct(productId, product) {
  const metafields = [
    { namespace: "shopmind", key: "emotion_tags", value: JSON.stringify(product.emotionTags), type: "json" },
    { namespace: "shopmind", key: "persona", value: JSON.stringify(product.personas), type: "json" },
    { namespace: "shopmind", key: "occasion", value: JSON.stringify(product.occasions), type: "json" },
    { namespace: "shopmind", key: "mood_tiles", value: JSON.stringify(product.moodTiles), type: "json" },
    { namespace: "shopmind", key: "social_proof", value: JSON.stringify(product.socialProof), type: "json" },
    { namespace: "shopmind", key: "complementary", value: JSON.stringify(product.complementary), type: "json" },
    { namespace: "shopmind", key: "regret_objection", value: product.regretObjection, type: "single_line_text_field" },
    { namespace: "shopmind", key: "regret_answer", value: product.regretAnswer, type: "single_line_text_field" },
  ];

  const query = `
    mutation MetafieldsSet($metafields: [MetafieldsSetInput!]!) {
      metafieldsSet(metafields: $metafields) {
        metafields { key namespace value }
        userErrors { field message }
      }
    }
  `;

  const input = metafields.map(mf => ({
    ownerId: productId,
    namespace: mf.namespace,
    key: mf.key,
    value: mf.value,
    type: mf.type,
  }));

  const data = await shopifyQuery(query, { metafields: input });
  if (data.metafieldsSet.userErrors.length) {
    throw new Error(data.metafieldsSet.userErrors.map(e => e.message).join(', '));
  }
  return data.metafieldsSet.metafields;
}

async function createProduct(p) {
  // Use Shopify REST API — always supports variants with price in one call
  const body = {
    product: {
      title: p.title,
      body_html: p.description,
      handle: p.handle,
      tags: p.tags.join(', '),
      status: 'active',
      variants: [{ price: p.price, inventory_management: null }],
      // Attach product image if provided
      ...(p.imageUrl ? { images: [{ src: p.imageUrl, alt: p.title }] } : {}),
    }
  };

  const res = await fetch(`${SHOPIFY_REST}/products.json`, {
    method: 'POST',
    headers: HEADERS,
    body: JSON.stringify(body),
  });

  const data = await res.json();

  if (!res.ok || data.errors) {
    const msg = data.errors
      ? JSON.stringify(data.errors).slice(0, 200)
      : `HTTP ${res.status}`;
    throw new Error(`Failed to create "${p.title}": ${msg}`);
  }

  // Return in GraphQL-compatible shape for metafield function
  const prod = data.product;
  return {
    id: `gid://shopify/Product/${prod.id}`,
    title: prod.title,
    handle: prod.handle,
    variants: {
      edges: prod.variants.map(v => ({
        node: { id: `gid://shopify/ProductVariant/${v.id}`, price: v.price }
      }))
    }
  };
}

async function seed() {
  console.log(`\n🌱  Seeding ${PRODUCTS.length} products to Shopify...\n`);
  let created = 0;
  let failed = 0;
  let firstError = null;

  for (const product of PRODUCTS) {
    try {
      process.stdout.write(`  → ${product.title}... `);
      const created_product = await createProduct(product);
      await createMetafieldsForProduct(created_product.id, product);
      console.log(`✓`);
      created++;
      // Small delay to avoid rate limiting
      await new Promise(r => setTimeout(r, 300));
    } catch (err) {
      console.log(`✗ ${err.message}`);
      failed++;
    }
  }

  console.log(`\n✅  Seeded ${created} products. ${failed > 0 ? `❌ ${failed} failed.` : ''}\n`);
  if (firstError) console.log('First error detail:', firstError);
}

